package Model;

public class Subjects
{
    String strSubCode;
    String strSubName;
    String strSubGrade;
    int strSubCredit;

    public Subjects(){}

    public String getStrSubCode() {
        return strSubCode;
    }

    public void setStrSubCode(String strSubCode) {
        this.strSubCode = strSubCode;
    }

    public String getStrSubName() {
        return strSubName;
    }

    public void setStrSubName(String strSubName) {
        this.strSubName = strSubName;
    }

    public String getStrSubGrade() {
        return strSubGrade;
    }

    public void setStrSubGrade(String strSubGrade) {
        this.strSubGrade = strSubGrade;
    }

    public int getStrSubCredit() {
        return strSubCredit;
    }

    public void setStrSubCredit(int strSubCredit) {
        this.strSubCredit = strSubCredit;
    }
}
